use legion::Resources;
use wgpu::{BindGroupLayout, Buffer, CommandEncoder, Device, Queue, RenderPass, RenderPipeline, TextureView};
use wgpu::util::DeviceExt;
use crate::rendering::pipeline::{DefaultPipeline};
use crate::rendering::scene::Drawable;

pub trait Model {
    type Pipeline;

    fn load(device: &Device, queue: &Queue, pipeline: &Self::Pipeline) -> Self where Self: Sized;
}

#[repr(C)]
#[derive(Copy, Clone, Debug, bytemuck::Pod, bytemuck::Zeroable)]
pub struct Vertex {
    pub(crate) position: [f32; 3],
    pub(crate) tex_coords: [f32; 2],
}

impl Vertex {
    pub fn desc<'a>() -> wgpu::VertexBufferLayout<'a> {
        wgpu::VertexBufferLayout {
            array_stride: std::mem::size_of::<Vertex>() as wgpu::BufferAddress,
            step_mode: wgpu::VertexStepMode::Vertex,
            attributes: &[
                wgpu::VertexAttribute {
                    offset: 0,
                    shader_location: 0,
                    format: wgpu::VertexFormat::Float32x3,
                },
                wgpu::VertexAttribute {
                    offset: std::mem::size_of::<[f32; 3]>() as wgpu::BufferAddress,
                    shader_location: 1,
                    format: wgpu::VertexFormat::Float32x2,
                },
            ],
        }
    }
}
